package com.cg.StudentDemo;

public class StudentClass {
	int rollNo;
	String firstName;
	String lastName;

	 public StudentClass(int rollNo, String firstName, String lastName)
	{
	this.rollNo = rollNo;
	this.firstName = firstName;
	this.lastName = lastName;
	}

	 public int getRollNo()
	{
	return rollNo;
	}

	 public String getFirstName()
	{
	return firstName;
	}

	 public String getLastName()
	{
	return lastName;
	}
	}

